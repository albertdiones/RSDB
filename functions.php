   }
   if (file_exists($d))
      $link->log="Successfuly Created $d";
   else
      $link->errorMessage="<b>Failed to create</b> $d";
   $link->select_table($table);
   if (!file_exists($link->getFileLocCol()))
      RSDB::mkdir($link->getFileLocCol());
