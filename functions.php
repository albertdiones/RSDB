function replica_write($f,$c) {
   if (file_exists($f))
   {
      if (!is_writable($f))
         @chmod($f,0777);
      if (is_writable($f))
      {
         $f=fopen($f,w);
         fwrite($f,$c);
         fclose($f);
         return true;
      }
   }
   else
   {
      $f=fopen($f,x);
      fwrite($f,$c);
      fclose($f);
      return true;
   }
}
